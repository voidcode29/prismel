/** Each shade is an RGB triplet string: "R G B" (no commas, no alpha).
 *  Used as `rgb(var(--key) / <alpha-value>)` in Tailwind config.
 *
 *  Shade semantics (same in light and dark mode):
 *    50  = lightest (bg in light, text in dark via dark:text-solaris-50)
 *    200 = light surface
 *    400 = muted text / borders
 *    600 = body text on light bg
 *    800 = dark surface (cards in dark via dark:bg-solaris-800)
 *    900 = dark elevated
 *    950 = darkest (bg in dark via dark:bg-solaris-950)
 *
 *  Dark palettes have the SAME shade values as light. The Tailwind `dark:`
 *  variant selects a different shade number for the appropriate color. */
export interface ThemePalette {
  solaris: string[];
  "solaris-blue": string[];
  "solaris-cyan": string[];
  "solaris-green": string[];
  "solaris-yellow": string[];
  "solaris-orange": string[];
  "solaris-red": string[];
  "solaris-violet": string[];
  "solaris-magenta": string[];
}

export interface ThemeVariant {
  name: string;
  id: string;
  preview: { light: string; dark: string };
  palette: {
    light: ThemePalette;
    dark: ThemePalette;
  };
}

